    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed

        // TODO add your handling code here:
        String workout = jWorkout.getText();
        String weightused = jWeightUsed.getText();
        String sets = jSets.getText();
        String reps = jReps.getText();
        String timetaken = jTimeTaken.getText();
        String date = jDate.getText();
        String query = "INSERT INTO nuviadb.workouts(`workout`,`weightused`,`sets`,`reps`, `timetaken`, `date`)VALUES(?,?,?,?,?,?)";

        try {

            PreparedStatement ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, workout);
            ps.setString(2, weightused);
            ps.setString(3, sets);
            ps.setString(4, reps);
            ps.setString(5, timetaken);
            ps.setString(6, date);
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "New Workout Added");
            }
        } catch (Exception ex) {
            System.out.println("error : " + ex.getMessage());
        }


    }//GEN-LAST:event_AddActionPerformed

