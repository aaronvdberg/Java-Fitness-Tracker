    private void ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewActionPerformed
        // TODO add your handling code here:

        try {

            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM nuviadb.workouts");
            ResultSet rs =ps.executeQuery();
            
            DefaultTableModel tm = (DefaultTableModel) jWorkouts.getModel();
            tm.setRowCount(0);
            
            while(rs.next()){
                Object o[] ={rs.getString("workout"), rs.getString("weightused"),rs.getString("sets"),rs.getString("reps"),rs.getString("timetaken"),rs.getString("date")};
                tm.addRow(o);
            }

        } catch (Exception ex) {
            System.out.println("error : " + ex.getMessage());
        }
    }//GEN-LAST:event_ViewActionPerformed

